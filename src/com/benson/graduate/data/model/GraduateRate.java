package com.benson.graduate.data.model;

public class GraduateRate extends Rate{
	
	
	private int 未毕业人数;
	private int 已毕业人数;
	
	public int get未毕业人数() {
		return 未毕业人数;
	}
	public void set未毕业人数(int 未毕业人数) {
		this.未毕业人数 = 未毕业人数;
	}
	public int get已毕业人数() {
		return 已毕业人数;
	}
	public void set已毕业人数(int 已毕业人数) {
		this.已毕业人数 = 已毕业人数;
	}
}
