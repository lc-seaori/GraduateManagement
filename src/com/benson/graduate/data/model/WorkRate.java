package com.benson.graduate.data.model;

public class WorkRate extends Rate {
	
	private int 未就业人数;
	private int 已就业人数;
	
	public int get未就业人数() {
		return 未就业人数;
	}
	public void set未就业人数(int 未就业人数) {
		this.未就业人数 = 未就业人数;
	}
	public int get已就业人数() {
		return 已就业人数;
	}
	public void set已就业人数(int 已就业人数) {
		this.已就业人数 = 已就业人数;
	}
	
	
	
}
